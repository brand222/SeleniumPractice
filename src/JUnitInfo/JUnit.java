package JUnitInfo;

public class JUnit {

	/*
	 * JUnit is a testing framework that comes out of the box
	 * with selenium Jar files. This comes OOTB with when you donwload
	 * selenium (open source).
	 * 
	 * Annotations:
	 * @BeforeClass: The method with this annotation will run
	 * before the first test method in the current class.
	 * 
	 * @AfterClass: The method with this annotation will run after the last
	 * test method in the current class.
	 * 
	 * @Before: The method with this annotation will run before each method
	 * 
	 * @After: The method with this annotation will run after each method
	 * 
	 * @Test: The method with this annotation means that it is a test.
	 * 
	 */
}
